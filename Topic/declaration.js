// Array Declaration is used to create a new array in JavaScript.
// There are several ways to declare an array, including using array literals and the Array constructor.

// Here is an example of how to declare arrays in JavaScript:
// Declaring an array using array literal syntax
let fruits = ['Apple', 'Banana', 'Cherry'];
console.log(fruits);
// Output: ['Apple', 'Banana', 'Cherry']

// Declaring an array using the Array constructor
let numbers = new Array(1, 2, 3, 4, 5);
console.log(numbers);
// Output: [1, 2, 3, 4, 5]

// Declaring an empty array and adding elements later
let colors = [];
colors.push('Red');
colors.push('Green');
colors.push('Blue');
console.log(colors);
// Output: ['Red', 'Green', 'Blue']